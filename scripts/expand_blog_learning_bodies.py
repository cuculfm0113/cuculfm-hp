CLOSING = """
## 読了後の一行メモ

この回の学びを140字以内で要約し、翌週の業務で一度だけ実践してみてください。音声メモに3分解説を録音し、未来の自分への手紙として残すと抜けが見えます。勉強会に使う場合はチェックリストを印刷して自己採点してから終えると定着します。公式ドキュメント・料金・UIは更新されるため、手順と数値は必ず最新情報で再確認してください。改善アクションは**1つに絞る**と実行率がもっとも高くなります。

連載を順に進めるときは「前章のチェックリストが緑になるまで次へ進まない」ペースが安全です。分からない用語はその場で検索せず、一度記録だけして後で3語に圧縮すると記憶に残ります。業務と学習の境目で迷ったら、公開情報とダミーデータだけで試すセーフティゾーンを先に決めてから触れてください。
"""


def _inject(content: str, extra: str) -> str:
    marker = "## 次にやること"
    idx = content.find(marker)
    if idx == -1:
        raise SystemExit("marker not found")
    return content[:idx] + extra.strip() + "\n\n" + content[idx:]


def main() -> None:
    for sub in ("blog/ai/manus", "blog/ai/n8n"):
        d = ROOT / sub
        for path in sorted(d.glob("*.md")):
            name = path.name
            if name not in EXTRA:
                raise SystemExit(f"Missing EXTRA for {name}")
            text = path.read_text(encoding="utf-8")
            if "## 学びを固める（追補）" in text:
                print(f"skip (already expanded): {path.relative_to(ROOT)}")
                continue
            text = _inject(text, EXTRA[name])
            path.write_text(text, encoding="utf-8")
            print(f"expanded: {path.relative_to(ROOT)}")


